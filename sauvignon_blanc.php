                    <strong>ORIGEN</strong>
                    <p>Este vino proviene de los mejores viñedos con los que Viña San Pedro cuenta actualmente en el Valle de Casablanca, el cual posee una importante influencia del Océano Pacifico, otorgándole un clima fresco ideal para la lenta maduración de las uvas que dan origen a este vino. El sistema de conducción de los viñedos es en espaldera vertical con riego por goteo controlado.</p>
                    <strong>VENDIMIA</strong>
                    <p>La uva se cosechó a mano, durante la segunda quincena de marzo y la primera semana de abril. El rendimiento del viñedo fue de 9 toneladas/ha.</p>
                    <strong>CLIMA</strong>
                    <p>La vendimia 2011 comenzó con una muy fría primavera, que retraso la brotación alrededor de 2 semanas en algunas áreas. Mas tarde en la temporada existió un leve aumento de las temperaturas durante el verano, pero no se alcanzaron las temperaturas máximas habituales, lo que retrasó la cosecha , pero a la vez esto permitió una madurez lenta de la uvas y genero vinos muy frescos, con buen nivel de acidez y gran intensidad aromática.</p>
                    <strong>VINIFICACION</strong>
                    <p>La uva se despalilló, enfrío y maceró en la prensa durante 4 a 6 horas a una temperatura de 8°C. Fermentación alcohólica 100% realizada en tanques de acero inoxidable realizada con levaduras seleccionadas y temperatura controlada entre 10 y 11.5° C</p>
