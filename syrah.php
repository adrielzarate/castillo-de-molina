                    <strong>ORIGEN</strong>
                    <p>Este vino es elaborado con las mejores uvas de los viñedos de Viña San Pedro situados en del Valle del Maule, específicamente en el área de Pencahue. Estos, se encuentran plantados en una zona de lomajes con pendientes suaves, de suelos franco arcillo arenosos a arcillosos moderadamente profundos, con presencia de pedregosidad superficial media. El sistema de conducción de los viñedos es en espaldera vertical con riego por goteo controlado.</p>
                    <strong>VENDIMIA</strong>
                    <p>La uva se cosechó a mano, entre la segunda y tercera semana de abril. El rendimiento del viñedo fue de 8.0 toneladas/ha.</p>
                    <strong>CLIMA</strong>
                    <p>La vendimia 2009 partió con una primavera muy fría lo cual atrasó la brotación alrededor de 3 semanas en algunas zonas. Las condiciones observadas en el transcurso de la primavera repercutieron en producciones moderadas a bajas, por lo que los rendimientos fueron más bajos que en años anteriores y permitió tener una excelente calidad de uvas al vinificar. Los vinos obtenidos son concentrados, de buen color y alta intensidad frutal.</p>
                    <strong>VINIFICACION</strong>
                    <p>Maceración en frío por 5 días. Fermentación alcohólica realizada con levaduras seleccionadas y temperatura controlada entre 25 a 27° C. Se realizó una fermentación poco extractiva, con el objetivo de obtener vinos sedosos y elegantes (Se remontó menos de un 50% del volumen al día). 25 días de maceración en total. Se realiza crianza en barricas de roble francés 20% nuevas, 40% de 1 uso y 40% de 2 usos, por un periodo de 10 meses.</p>
