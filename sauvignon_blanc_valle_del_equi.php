                    <strong>ORIGEN</strong>
                    <p>Este vino proviene de los mejores viñedos con los que Viña San Pedro cuenta actualmente en el Valle de Elqui, de suelos Franco areno limosos con presencia de piedras anguladas en todo el perfil. El viñedo presenta una fuerte influencia marina, ya que esta ubicado aproximadamente a 20 kilómetros en línea recta del mar. Todas las mañanas ingresa por la cuenca del río Elqui una nubosidad proveniente del mar, la cual se establece sobre los viñedos hasta aproximadamente medio día, favoreciendo el clima fresco y una buena ventilación debido también a lo ventoso del área. El sistema de conducción de los viñedos es en espaldera vertical con riego por goteo.</p>
                    <strong>VENDIMIA</strong>
                    <p>La uva se cosechó a mano, en cajas de 12 kilos, la segunda quincena de marzo. El rendimiento del viñedo fue de 10 toneladas/ha.</p>
                    <strong>CLIMA</strong>
                    <p>La vendimia 2010 fue una vendimia bastante atípica, que partió con una primavera muy fría lo cual atrasó la brotación alrededor de 3 a 4 semanas en algunas zonas. Posteriormente las temperaturas en verano fueron menores que los años anteriores. Los vinos blancos obtenidos son frescos, de muy buena acidez y aromas intensos y maduros.</p>
                    <strong>VINIFICACION</strong>
                    <p>La uva se seleccionó en mesa de selección, luego se despalilló, enfrió y maceró en la prensa durante 4 a 6 horas a una temperatura de 8°C. Fermentación alcohólica realizada con levaduras seleccionadas y temperatura controlada entre 10 y 11° C. Manejo totalmente reductivo.</p>
