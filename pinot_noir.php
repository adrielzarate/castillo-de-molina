                    <strong>ORIGEN</strong>
                    <p>Este vino es elaborado con uvas de Pinot Noir provenientes del Valle de Curicó. Los viñedos están plantados en una zona de lomajes suaves. El sistema de conducción de los viñedos es en espaldera vertical con riego por goteo.</p>
                    <strong>VENDIMIA</strong>
                    <p>La uva se cosechó a mano entre la ultima semana de marzo y la primera de abril. El rendimiento del viñedo fue de 7,5 toneladas/ha.</p>
                    <strong>CLIMA</strong>
                    <p>Al igual que en otras variedades el 2009 fue un año atípico, donde las temperaturas máximas fueron más bajas que un año normal. Las variedades tintas, en general, se cosecharon al menos 5 días más tarde que lo usual. La disminución de las temperaturas máximas permitió una madurez lenta de la uva, lo que genero vinos de alta intensidad aromática. La acidez fue más alta que otros años, lo que favoreció el color y la frescura.</p>
                    <strong>VINIFICACION</strong>
                    <p>Maceración en frío por 5 días. Fermentación alcohólica realizada con levaduras seleccionadas y temperatura controlada entre 25 a 27° C. Se realizan 3 pisoneos por día. 15 días de maceración en total. Se realiza guarda en barricas de roble francés 30% nuevas, 35% de 1 uso y 35% de 2 usos- La crianza se realiza por un periodo de 10 meses.</p>
