                    <strong>ORIGEN</strong>
                    <p>Este vino proviene de los mejores viñedos de Viña San Pedro en el Valle de Casablanca, el cual posee una importante influencia del Océano Pacifico que le otorga un clima fresco, ideal para la maduración lenta de las uvas que dan origen a este vino. El sistema de conducción de los viñedos es en espaldera vertical, con riego por goteo.</p>
                    <strong>VENDIMIA</strong>
                    <p>La uva se cosechó a mano, la segunda semana de abril. El rendimiento del viñedo fue de 9.0 toneladas/ha.</p>
                    <strong>CLIMA</strong>
                    <p>La vendimia 2010 fue atípica y tranquila. Comenzó con una muy fría primavera, que retraso la brotación alrededor de 3 a 4 semanas en algunas áreas. Mas tarde en la temporada existió un leve aumento de las temperaturas durante el verano, pero no se alcanzaron las temperaturas máximas habituales, sobre todo en las zonas frías con mayor influencia oceánica, lo que permitió una madurez lenta de la uvas, y esto genero vinos muy frescos, con buen nivel de acidez y gran intensidad de aromas.</p>
                    <strong>VINIFICACION</strong>
                    <p>50% prensado directo y 50% racimo despalillado. Fermentación alcohólica realizada con levaduras seleccionadas y temperatura controlada entre 15°C y 16°C. El 70% de la mezcla fue fermentado en barricas de roble francés, y luego se realizo crianza del vino durante 6 meses en las mismas barricas sin trasegar y con bastoneo 2 veces por semanas. El 30% restante se fermentó y se mantuvo en acero inoxidable, para aportar frescura y fruta a la mezcla final. No se realizó fermentación maloláctica.</p>
