                    <strong>ORIGEN</strong>
                    <p>Este vino es elaborado con las mejores uvas de Merlot provenientes de los viñedos que Viña San Pedro posee en el Valle de Rapel. Los viñedos están plantados en el piedmont de la Cordillera de los Andes, sobre suelos arcillosos con pendientes entre 5 y 15%. El sistema de conducción de los viñedos es en espaldera vertical con riego por goteo.</p>
                    <strong>VENDIMIA</strong>
                    <p>La uva se cosechó a mano, la primera semana de abril. El rendimiento del viñedo fue de 9.0 toneladas/ha.</p>
                    <strong>CLIMA</strong>
                    <p>Al igual que en otras variedades el 2009 fue un año atípico, donde las temperaturas máximas fueron más bajas que un año normal. Las condiciones observadas en el transcurso de la primavera repercutieron en producciones moderadas a bajas, por lo que los rendimientos fueron más bajos que en años anteriores y permitió tener una excelente calidad de uvas al vinificar. Las variedades tintas, en general, se cosecharon al menos 5 días más tarde que lo usual. La acidez fue más alta que otros años, lo que favoreció el color y la frescura.</p>
                    <strong>VINIFICACION</strong>
                    <p>Maceración en frío por 3 días. Fermentación alcohólica realizada con levaduras seleccionadas y temperatura controlada entre 25 a 27° C. Se realizaron 5 remontajes cortos por día. 20 a 25 días de maceración en total. Se realiza crianza por un periodo de 10 meses en barricas de roble francés 30% nuevas, 35% de 1 uso y 35% de 2 usos.</p>
